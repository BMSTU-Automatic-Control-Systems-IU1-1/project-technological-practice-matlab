X = linspace(-0.5, 0.5, 100);
F =  abs(2 * X) .^ 3;
G = 2 * X .^ 5;
plot(X, F, '+red-');
hold on
plot(X, G, 'pblue:');
hold off
grid on
plotyy(X, F, X, G);
xlabel('ось x');
ylabel('ось y');
legend('abs(2x)^3', '2x^5');
